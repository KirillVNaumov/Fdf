#include "fdf.h"

int		find_width(char *s)
{
	int i;
	int count;

	i = 0;
	count = 0;
	while (s[i])
	{
		while (s[i] && s[i] == ' ')
			i++;
		count++;
		while (s[i] && s[i] != ' ')
			i++;
	}
	return (count);
}
